                    <div class="frame-item line">
                        <i class="fa fa-hand-o-right"></i>
                        <span>Wide choices LED garage light</span>
                    </div>
                    <p>EchoLumen supplies a wide variety of LED garage light with wide power consumption from 30W to 120W for any indoor locations where require different suspension height. From tight space basement/garage to wide open space office, warehouse or stadium, EchoLumen LED garage light can meet all your expectations on the quality and lighting effect.</p>
                    <div class="frame-item line">
                        <i class="fa fa-hand-o-right"></i>
                        <span>Deformable and adjustable LED garage light</span>
                    </div>
