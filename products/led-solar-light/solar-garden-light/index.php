                    <h3>Chapter 1  What is LED garden light or solar garden light?</h3>
                    <p>LED garden light or solar garden light is using LED as its light source and assembled with solar panel at top of the lamp to receive sunlight and transformed it into electric power and restored in the battery placed inside of the lamp body. A light sensor is integrated into the solar panel, in the daytime, the solar panel will charge the battery and the LED garden light or solar garden light will be light up automatically when the ambient light is getting dark. The market demand on LED garden light or solar garden light is massive since such product is cheap in price and low cost to maintain.</p>
                    <h3>Chapter 2 Types of EchoLumen LED garden light or solar garden light</h3>
                    <div class="square">
                        <img src="../../../images/category/solar-garden-light-3.jpg" />
                    </div>
                    <p>EchoLumen has full ranges of LED garden light or solar garden light products and is capable of supplying the turn key solutions for the lighting projects at private garden, household lawn, public park, commercial street and cultural square etc. We have two main categories of LED garden light or solar garden light with different installations. </p>
                    <p>One is LED spotlight or lawn lamp, which you can simply insert its base into ground and expose its solar panel in the sunlight. It is perfect for the installation at lawn, private garden, sides of sidewalks in recreational and residential area for lighting decoration.</p>
                    <p>Another is pillar LED solar light, which can be easily mounted to the platform of pillars at residential and recreational area, scenery spots and promenade by the sea or lakes etc.</p>
