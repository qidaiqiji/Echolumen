                    <p>EchoLumen LED garden light or solar garden light is small in size, exquisitely designed and easy to be installed, especially the LED spot light/lawn lamp which requires no tools for installation.</p>
                    <div class="frame-item line">
                        <i class="fa fa-hand-o-right"></i>
                        <span>Intelligent and practical function LED garden light or solar garden light</span>
                    </div>
                    <p>EchoLumen LED garden light or solar garden light is equipped with one solar panel which integrated one light sensor inside. In the daytime, the solar panel will charge the battery and the LEDs will be light up automatically when it is getting dark.</p>
                    <div class="frame-item line">
                        <i class="fa fa-hand-o-right"></i>
                        <span>Ultra high efficient solar charging and large capacity battery</span>
                    </div>
                    <p>EchoLumen LED garden light or solar garden light is with high quality solar panel, the battery can be fully charged within 5-7 hours in the sunlight and its lighting time can be lasting 3 successive nights (12 hours per night).</p>
